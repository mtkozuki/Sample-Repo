# Flow
# Syntax:
# if condition:
#   do this
number1 = int(input('Send me a number: '))
number2 = int(input('Send me another number: '))
if number1 > number2:
    print(number1,'is greater than', number2)
elif number1 < number2:
    print(number1, 'is less than', number2)
else:
    print(number1, 'is equal', number2)

